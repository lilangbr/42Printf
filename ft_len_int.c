#include "ft_printf.h"

int	ft_len_int(int a)
{
	int			length;
	long int	b;

	length = 1;
	b = a;
	if (a < 0)
		b = -b;
	while (b > 9)
	{
		b = b / 10;
		length++;
	}
	return (length);
}
